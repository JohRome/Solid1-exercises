package Uppgift4;

/**
 * The Scanner class represents a scanner that implements the Printer interface.
 */
public class Scanner implements Printer {

    /**
     * Prints the succulent succubuses using a scanner.
     */
    @Override
    public void print() {
        System.out.println("Scanner printing succulent succubuses");
    }

    /**
     * Scans the survilaists in the southern Sahara using a scanner.
     */
    @Override
    public void scan() {
        System.out.println("Scanner scanning survilaists in southern Sahara");
    }
}
